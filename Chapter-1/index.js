// Chapter-1 Values, Types, and Operators

console.log(8 * null);
// → 0
console.log("5" - 1);
// → 4
console.log("5" + 1);
// → 51
console.log("five" * 2);
// → NaN
console.log(false == 0);
// → true
console.log(null == undefined);
// → true
console.log(null == 0);
// → false
console.log(NaN == NaN);
// → false

console.log(null || "user");
// → user
console.log("Agnes" || "user");
// → Agnes

